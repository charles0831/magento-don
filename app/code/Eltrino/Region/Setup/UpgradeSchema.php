namespace Eltrino\Region\Setup;

use Magento\Framework\Setup\UpgradeSchemaInterface;
use Magento\Framework\Setup\ModuleContextInterface;
use Magento\Framework\Setup\SchemaSetupInterface;

/**
 * Class UpgradeSchema
 * @package Eltrino\Region\Setup
 */
class UpgradeSchema implements UpgradeSchemaInterface
{
    /**
     * @param SchemaSetupInterface $setup
     * @param ModuleContextInterface $context
     * @throws \Zend_Db_Exception
     */
    public function upgrade(SchemaSetupInterface $setup, ModuleContextInterface $context)
    {
        $setup->startSetup();

        if (version_compare($context->getVersion(), '2.1.0', '<')) {
            $oldTableName = $setup->getTable('eltrino_disabled_region');
            $newTableName = $setup->getTable('eltrino_region_entity');
            if ($setup->tableExists("eltrino_disabled_region")) {
                $setup->getConnection()->renameTable($oldTableName, $newTableName);
            }
        }
        $setup->endSetup();
    }
}
